#include<iostream>
using namespace std;
int main(){
    int x=4;
    cout<<x<<endl;
    x++;
    cout<<x<<endl;
//Decrement
int y=4;
cout<<y<<endl;
y--;
cout<<y<<endl;
//Post Increment
cout<<x<<endl;
cout<<x++<<endl;
cout<<x<<endl<<endl;
//Pre Increment
cout<<"Example of Pre Increment";
cout<<x<<endl;
cout<<++x<<endl;
cout<<x<<endl<<endl;
//Post Decrement
cout<<x<<endl;
cout<<x--<<endl;
cout<<x<<endl<<endl;
//Pre Decrement
cout<<"Example of Pre Decrement";
cout<<x<<endl;
cout<<--x<<endl;
cout<<x<<endl<<endl;

}



